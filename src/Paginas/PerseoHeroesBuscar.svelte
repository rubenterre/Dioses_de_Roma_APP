        Perseo era el hijo de Zeus y Danaë, la hija de Acrisius, rey de Argos. Decepcionado por su falta de suerte en
        tener un hijo, Acrisio consultó al oráculo de Delfos, quien le advirtió que algún día sería asesinado por el
